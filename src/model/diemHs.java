package model;

public class DiemHs {
    private int maHS;
    private int maMon;
    private float diemToan;
    private float diemNguVan;
    private float diemNgoaiNgu;
    private float diemGDCD;
    private float diemLichSu;
    private float diemTinHoc;
    private float diemTheDuc;
    private float diemKhoaHoc;
    public DiemHs(int maHS, int maMon, float diemToan, float diemNguVan, float diemNgoaiNgu, float diemGDCD,
            float diemLichSu, float diemTinHoc, float diemTheDuc, float diemKhoaHoc) {
        this.maHS = maHS;
        this.maMon = maMon;
        this.diemToan = diemToan;
        this.diemNguVan = diemNguVan;
        this.diemNgoaiNgu = diemNgoaiNgu;
        this.diemGDCD = diemGDCD;
        this.diemLichSu = diemLichSu;
        this.diemTinHoc = diemTinHoc;
        this.diemTheDuc = diemTheDuc;
        this.diemKhoaHoc = diemKhoaHoc;
    }
    public int getMaHS() {
        return maHS;
    }
    public void setMaHS(int maHS) {
        this.maHS = maHS;
    }
    public int getMaMon() {
        return maMon;
    }
    public void setMaMon(int maMon) {
        this.maMon = maMon;
    }
    public float getDiemToan() {
        return diemToan;
    }
    public void setDiemToan(float diemToan) {
        this.diemToan = diemToan;
    }
    public float getDiemNguVan() {
        return diemNguVan;
    }
    public void setDiemNguVan(float diemNguVan) {
        this.diemNguVan = diemNguVan;
    }
    public float getDiemNgoaiNgu() {
        return diemNgoaiNgu;
    }
    public void setDiemNgoaiNgu(float diemNgoaiNgu) {
        this.diemNgoaiNgu = diemNgoaiNgu;
    }
    public float getDiemGDCD() {
        return diemGDCD;
    }
    public void setDiemGDCD(float diemGDCD) {
        this.diemGDCD = diemGDCD;
    }
    public float getDiemLichSu() {
        return diemLichSu;
    }
    public void setDiemLichSu(float diemLichSu) {
        this.diemLichSu = diemLichSu;
    }
    public float getDiemTinHoc() {
        return diemTinHoc;
    }
    public void setDiemTinHoc(float diemTinHoc) {
        this.diemTinHoc = diemTinHoc;
    }
    public float getDiemTheDuc() {
        return diemTheDuc;
    }
    public void setDiemTheDuc(float diemTheDuc) {
        this.diemTheDuc = diemTheDuc;
    }

    public float getDiemKhoaHoc() {
        return diemKhoaHoc;
    }
    public void setDiemKhoaHoc(float diemKhoaHoc) {
        this.diemKhoaHoc = diemKhoaHoc;
    }
    
}
