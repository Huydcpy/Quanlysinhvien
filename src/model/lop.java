package model;

public class lop {
    private int maLop;
    private String tenLop;
    private int khoi;
    public lop(int maLop, String tenLop, int khoi) {
        this.maLop = maLop;
        this.tenLop = tenLop;
        this.khoi = khoi;
    }
    public int getMaLop() {
        return maLop;
    }
    public void setMaLop(int maLop) {
        this.maLop = maLop;
    }
    public String getTenLop() {
        return tenLop;
    }
    public void setTenLop(String tenLop) {
        this.tenLop = tenLop;
    }
    public int getKhoi() {
        return khoi;
    }
    public void setKhoi(int khoi) {
        this.khoi = khoi;
    }
    

}
